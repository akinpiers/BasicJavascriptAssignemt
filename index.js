function addition(num1,num2) {
//Addition operation
    let result = num1 + num2

    return result

}

console.log(addition(1000,1000))

//Substraction

function Substraction(num1,num2){

    let result = num1 - num2
    return result
}

console.log(Substraction(10000,500))

//Division

function division(num1,num2){

    let result = num1/num2
    return result
}

console.log(division(10000,50))

//Modulus

function modulus(num1,num2){

    let result = num1%num2
    return result
}

console.log(modulus(100,40))